void CTubeCfg::SetOEMBox(CComboBox *ptCCB, TUBINGCFG *ptTubing, UINT nLang)
{
    int     i       = 0;
    int     j       = 0;
    int     iCur    = 0;
    set<UINT>  OEMs;
    set<UINT>::iterator it;

    ASSERT_NULL(ptCCB);
    ASSERT_NULL(ptTubing);
    COMP_BGE(nLang, LANGUAGE_NUM);

    ptCCB->ResetContent();
    for(i=0; i<MAXTUBINGNUM; i++)
    {
        if(g_tTubingCfg[i].nFactory != ptTubing->nFactory)
            continue;

        OEMs.insert(g_tTubingCfg[i].nOEM);
    }
    j=0;
    for(it=OEMs.begin();it!=OEMs.end();it++)
    {
        for(i=0; i<MAXOEMNUM; i++)
        {
            if(g_tOEM[i].nNO== (*it))
            {
                ptCCB->AddString(g_tOEM[i].strName[nLang].c_str());
                break;
            }
        }
        if(ptTubing->nOEM == (*it))
            iCur = j;
        j++;
    }
    OEMs.clear();
    ptCCB->SetCurSel(iCur);
}

