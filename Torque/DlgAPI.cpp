    rcGrid.top += iBarHigh;
    m_pGridCtrl->Create(rcGrid, this, IDI_APIGRID);
    ASSERT_NULL_R(m_pGridCtrl->GetSafeHwnd(), FALSE); 

    //ÉèÖÃGridCtrlÊôÐÔ¡¢ÐÐÁÐ¼°µÚÒ»ÐÐÐÐÍ·ÄÚÈÝ
    SetGridRowCol();

    m_pGridCtrl->AutoSize();
    // m_pGridCtrl->SetRowHeight(0, 3*m_pGridCtrl->GetRowHeight(0)/2);

    /* ÉèÖÃµÚ¶þÐÐÐÐÍ·ÄÚÈÝ */
