string GetCurTime()
{
    time_t t = time(NULL);
    struct tm local;
    char  buff[20] = { 0 };

    localtime_s(&local, &t);
    strftime(buff, 20, "%Y-%m-%d %H:%M:%S", &local);

    return string(buff);
}

