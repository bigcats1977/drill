vector<int> GetIDFromList(string lsVals)
{
    vector<int> Vals;
    char delimiter = ',';
    string val;

    stringstream sstream(lsVals);
    while (getline(sstream, val, delimiter))
    {
        if (!val.empty())
        {
            if (val == " ")
            {
                Vals.push_back(DB_INVALID_VAL);
            }
            else
            {
                Vals.push_back(stoi(val));
            }

        }
    }
    return Vals;
}

